static void
poisspmf_print(double lambda)
{
	printf("lambda = %g\n", lambda);
	for (volatile int i = 0; i <= 20; i++)
	{
		printf("% 3d : %15.15f\n", i, poisspmf(i, lambda));
	}
	puts("");
}
