/* 単精度複素数型を虚数i単位で文字列に変換する。 */
char *
c_ftosi(fcomplex z)
{
	static char s[0x200];
	char *ptr = s;
	int size;
	
	*ptr++ = '(';
	switch (fpclassify(z.real)) {
	case FP_NAN:
		*ptr++ = 'N'; *ptr++ = 'a'; *ptr++ = 'N';
		break;
	case FP_INFINITE:
		if (z.real < 0)  *ptr++ = '-';
		*ptr++ = 'I'; *ptr++ = 'n'; *ptr++ = 'f';
		*ptr++ = 'i'; *ptr++ = 'n'; *ptr++ = 'i';
		*ptr++ = 't'; *ptr++ = 'y';
		break;
	case FP_ZERO:
		if (signbit(z.real))  *ptr++ = '-';
		*ptr++ = '0'; *ptr++ = '.'; *ptr++ = '0';
		break;
	default:
		size = snprintf(ptr, 0x200, "%.*g", FLT_DIG, z.real);
		for (volatile int i = 0; i < size; i++)  *ptr++;
		break;
	}
	switch (fpclassify(z.imag)) {
	case FP_NAN:
		*ptr++ = '+';
		*ptr++ = 'N'; *ptr++ = 'a'; *ptr++ = 'N';
		*ptr++ = '*';
		break;
	case FP_INFINITE:
		if (z.real < 0)  *ptr++ = '-';
		else             *ptr++ = '+';
		*ptr++ = 'I'; *ptr++ = 'n'; *ptr++ = 'f';
		*ptr++ = 'i'; *ptr++ = 'n'; *ptr++ = 'i';
		*ptr++ = 't'; *ptr++ = 'y';
		*ptr++ = '*';
		break;
	case FP_ZERO:
		if (signbit(z.real))  *ptr++ = '-';
		else                  *ptr++ = '+';
		*ptr++ = '0'; *ptr++ = '.'; *ptr++ = '0';
		break;
	default:
		size = snprintf(ptr, 0x200, "%+.*g", FLT_DIG, z.imag);
		for (volatile int i = 0; i < size; i++)  *ptr++;
		break;
	}
	*ptr++ = 'i';
	*ptr++ = ')'; *ptr++ = '\0';
	return s;
}

