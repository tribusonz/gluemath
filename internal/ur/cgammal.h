static inline lcomplex ur_cgammal(lcomplex x);

static inline lcomplex
cgammal_core(lcomplex z)
{
	fcomplex y;
	switch (fpclassify(z.imag)) {
	case FP_NAN:
		y.real = NAN; y.imag = NAN;
		break;
	case FP_INFINITE:
		if (z.imag < 0)  y.real = NAN;
		else             y.real = HUGE_VALL;
		y.imag = NAN;
		break;
	case FP_ZERO:
	case FP_SUBNORMAL:
		y.real = gammal_core(z.real);
		y.imag = 0;
		break;
	default:
		y = ur_cgammal(z);
		break;
	}
	return y;
}

static inline lcomplex
lc_divadd2(lcomplex t1, lcomplex z, long double t2, long double t3)
{
	z.real += t2;
	z = lc_div(ldtolc(t1), z);
	z.real += t3;
	return z;
}

